  <h2>Tipos de Redes Neurais</h2>

  <div class="highlight">
    <h3>Perceptron Multicamadas (MLP)</h3>
    <p>É uma rede neural totalmente conectada, composta por camadas densas. Ideal para tarefas de classificação e regressão simples.</p>
  </div>

  <div class="highlight">
    <h3>Redes Convolucionais (CNN)</h3>
    <p>São redes projetadas para processar dados com estrutura de grade, como imagens. Utilizam filtros para detectar padrões visuais.</p>
  </div>

  <div class="highlight">
    <h3>Redes Recorrentes (RNN)</h3>
    <p>Projetadas para lidar com dados sequenciais, como textos ou séries temporais. Possuem memória para considerar entradas anteriores.</p>
  </div>

  <div class="highlight">
    <h3>LSTM e GRU</h3>
    <p>Tipos avançados de RNN que lidam melhor com dependências de longo prazo. São amplamente usados em tradução automática e geração de texto.</p>
  </div>

  <div class="highlight">
    <h3>Transformers</h3>
    <p>Modelos modernos baseados em atenção. Substituíram as RNNs em tarefas de linguagem natural. São a base de sistemas como o ChatGPT.</p>
  </div>

  <div class="highlight">
    <h3>Autoencoders</h3>
    <p>Redes que aprendem a reconstruir suas entradas. Muito usados para compressão de dados, remoção de ruídos e redução de dimensionalidade.</p>
  </div>
